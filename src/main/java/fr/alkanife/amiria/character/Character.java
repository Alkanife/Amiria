package fr.alkanife.amiria.character;

public abstract class Character {

    public abstract String lastName();
    public abstract String name();
    public abstract String title();
    public abstract String race();
    public abstract String birth();
    public abstract String age();
    public abstract String sex();
    public abstract int floor();
    public abstract String specialization();
    public abstract String weapon();
    public abstract String secondaryWeapon();
    public abstract String characterTraits();
    public abstract String aspect();
    public abstract String ownPower();
    public abstract String state();
    public abstract String owner();
    public abstract String ownerInspiration();
    public abstract String image();

}
